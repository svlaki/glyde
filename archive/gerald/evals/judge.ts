/**
 * LLM-as-judge evaluation for interaction and response quality.
 * Uses a different model (GPT-4o) than Gerald (GPT-5.1) to avoid self-evaluation bias.
 * Includes retry logic with exponential backoff for rate limits.
 */

import OpenAI from 'openai';
import type {
  EvalConfig,
  LoadedUserData,
  GeneratedInteraction,
  ResponseProcessingResult,
  InteractionScore,
  ResponseScore,
} from './types.js';

const MAX_RETRIES = 3;
const JUDGE_BATCH_SIZE = 5;

const INTERACTION_RUBRIC = `You are evaluating the quality of a proactive interaction generated by an AI life assistant.

USER CONTEXT:
{userContext}

GENERATED INTERACTION:
Question: {question}
Type: {type}
Options: {options}
Priority: {priority}
Aspect: {aspect}
Metadata: {metadata}

Score each dimension from 1 (poor) to 5 (excellent):

1. CONTEXT RELEVANCE: How well does this relate to the user's actual schedule, tasks, goals, situation?
   1 = Completely irrelevant  |  5 = Deeply connected to user's specific situation

2. SPECIFICITY: Is the question concrete and actionable?
   1 = Vague/generic  |  5 = Highly specific to the user

3. APPROPRIATENESS: Is the interaction type correct? Timing right? Priority reasonable?
   1 = Wrong type or bad timing  |  5 = Perfect type, timing, and priority

4. METADATA QUALITY: Does metadata provide enough context for response processing?
   1 = Missing/useless metadata  |  5 = Complete with context, eventTitle, IDs

5. OVERALL: Would the user find this genuinely helpful?
   1 = Would annoy the user  |  5 = Would genuinely improve their day

Respond ONLY with valid JSON (no markdown, no explanation outside JSON):
{"contextRelevance": <1-5>, "specificity": <1-5>, "appropriateness": <1-5>, "metadataQuality": <1-5>, "overall": <1-5>, "reasoning": "<2-3 sentence explanation>"}`;

const RESPONSE_RUBRIC = `You are evaluating how well an AI assistant handled a user's response to an interaction.

ORIGINAL INTERACTION:
Question: {question}
Type: {type}
Options: {options}
Metadata: {metadata}

USER'S RESPONSE: "{userResponse}"
RESPONSE CATEGORY: {responseCategory}

ACTIONS TAKEN (tool calls):
{toolCalls}

Score each dimension from 1 (poor) to 5 (excellent):

1. ACTION CORRECTNESS: Did the assistant take the semantically correct action?
   - "Yes" to scheduling -> should create_event
   - "No" / "Skip" -> should do NOTHING (no tool calls)
   - Rating number -> already auto-stored, should do nothing
   - Text about event focus -> should update_event description
   - Text reflection -> should manage_patterns
   1 = Wrong action  |  5 = Perfect action

2. TOOL SELECTION: Was the right tool used?
   1 = Wrong tool entirely  |  5 = Exactly the right tool(s)

3. MINIMAL ACTION: Did the assistant avoid over-creating?
   - Should NOT create both task AND event for same thing
   - Should NOT create follow-up interactions
   - Decline = zero tool calls
   1 = Over-created  |  5 = Minimal and appropriate

4. OVERALL: How well was this response handled?
   1 = Incorrect handling  |  5 = Perfect handling

Respond ONLY with valid JSON (no markdown, no explanation outside JSON):
{"actionCorrectness": <1-5>, "toolSelection": <1-5>, "minimalAction": <1-5>, "overall": <1-5>, "reasoning": "<2-3 sentence explanation>"}`;

export async function judgeInteractions(
  interactions: readonly GeneratedInteraction[],
  userData: LoadedUserData,
  config: EvalConfig,
): Promise<readonly InteractionScore[]> {
  const openai = new OpenAI();
  const userContext = buildUserContextSummary(userData);
  const scores: InteractionScore[] = [];

  console.log(`[judge] Scoring ${interactions.length} interactions (batch size: ${JUDGE_BATCH_SIZE})`);

  for (let i = 0; i < interactions.length; i += JUDGE_BATCH_SIZE) {
    const batch = interactions.slice(i, i + JUDGE_BATCH_SIZE);
    const batchScores = await Promise.all(
      batch.map(interaction => judgeSingleInteraction(openai, interaction, userContext, config))
    );
    scores.push(...batchScores);
    console.log(`[judge] Scored ${Math.min(i + JUDGE_BATCH_SIZE, interactions.length)}/${interactions.length} interactions`);
    if (i + JUDGE_BATCH_SIZE < interactions.length) {
      await delay(2000);
    }
  }

  return scores;
}

export async function judgeResponses(
  results: readonly ResponseProcessingResult[],
  interactions: readonly GeneratedInteraction[],
  config: EvalConfig,
): Promise<readonly ResponseScore[]> {
  const openai = new OpenAI();
  const scores: ResponseScore[] = [];

  console.log(`[judge] Scoring ${results.length} responses (batch size: ${JUDGE_BATCH_SIZE})`);

  for (let i = 0; i < results.length; i += JUDGE_BATCH_SIZE) {
    const batch = results.slice(i, i + JUDGE_BATCH_SIZE);
    const batchScores = await Promise.all(
      batch.map(result => {
        const interaction = interactions[result.interactionIndex];
        return judgeSingleResponse(openai, result, interaction, config);
      })
    );
    scores.push(...batchScores);
    console.log(`[judge] Scored ${Math.min(i + JUDGE_BATCH_SIZE, results.length)}/${results.length} responses`);
    if (i + JUDGE_BATCH_SIZE < results.length) {
      await delay(2000);
    }
  }

  return scores;
}

async function judgeSingleInteraction(
  openai: OpenAI,
  interaction: GeneratedInteraction,
  userContext: string,
  config: EvalConfig,
): Promise<InteractionScore> {
  const prompt = INTERACTION_RUBRIC
    .replace('{userContext}', userContext)
    .replace('{question}', interaction.question)
    .replace('{type}', interaction.type)
    .replace('{options}', JSON.stringify(interaction.options || []))
    .replace('{priority}', String(interaction.priority))
    .replace('{aspect}', interaction.aspectId || 'none')
    .replace('{metadata}', JSON.stringify(interaction.metadata || {}));

  return retryWithBackoff(async () => {
    const response = await openai.chat.completions.create({
      model: config.judgeModel,
      temperature: 0,
      max_tokens: 300,
      messages: [{ role: 'user', content: prompt }],
    });

    const content = response.choices[0]?.message?.content?.trim() || '';
    return parseInteractionScore(content);
  }, () => defaultInteractionScore('Judge API error after retries'));
}

async function judgeSingleResponse(
  openai: OpenAI,
  result: ResponseProcessingResult,
  interaction: GeneratedInteraction,
  config: EvalConfig,
): Promise<ResponseScore> {
  const toolCallsStr = result.toolCalls.length === 0
    ? '(No tool calls - the assistant did nothing)'
    : result.toolCalls.map(tc => `  ${tc.name}(${JSON.stringify(tc.args)})`).join('\n');

  const prompt = RESPONSE_RUBRIC
    .replace('{question}', interaction.question)
    .replace('{type}', interaction.type)
    .replace('{options}', JSON.stringify(interaction.options || []))
    .replace('{metadata}', JSON.stringify(interaction.metadata || {}))
    .replace('{userResponse}', result.simulatedResponse.responseText)
    .replace('{responseCategory}', result.simulatedResponse.responseCategory)
    .replace('{toolCalls}', toolCallsStr);

  return retryWithBackoff(async () => {
    const response = await openai.chat.completions.create({
      model: config.judgeModel,
      temperature: 0,
      max_tokens: 300,
      messages: [{ role: 'user', content: prompt }],
    });

    const content = response.choices[0]?.message?.content?.trim() || '';
    return parseResponseScore(content);
  }, () => defaultResponseScore('Judge API error after retries'));
}

async function retryWithBackoff<T>(
  fn: () => Promise<T>,
  fallback: () => T,
): Promise<T> {
  for (let attempt = 0; attempt < MAX_RETRIES; attempt++) {
    try {
      return await fn();
    } catch (error: any) {
      const isRateLimit = error?.status === 429 || error?.message?.includes('429');
      if (isRateLimit && attempt < MAX_RETRIES - 1) {
        const backoffMs = Math.pow(2, attempt + 1) * 1000 + Math.random() * 1000;
        console.warn(`[judge] Rate limited, retrying in ${Math.round(backoffMs / 1000)}s (attempt ${attempt + 1}/${MAX_RETRIES})`);
        await delay(backoffMs);
      } else if (attempt === MAX_RETRIES - 1) {
        console.error(`[judge] Failed after ${MAX_RETRIES} attempts: ${error?.message}`);
        return fallback();
      }
    }
  }
  return fallback();
}

function parseInteractionScore(raw: string): InteractionScore {
  try {
    const cleaned = raw.replace(/```json\n?/g, '').replace(/```\n?/g, '').trim();
    const parsed = JSON.parse(cleaned);
    return {
      contextRelevance: clamp(parsed.contextRelevance, 1, 5),
      specificity: clamp(parsed.specificity, 1, 5),
      appropriateness: clamp(parsed.appropriateness, 1, 5),
      metadataQuality: clamp(parsed.metadataQuality, 1, 5),
      overall: clamp(parsed.overall, 1, 5),
      reasoning: parsed.reasoning || '',
    };
  } catch {
    return defaultInteractionScore(`Parse error: ${raw.substring(0, 100)}`);
  }
}

function parseResponseScore(raw: string): ResponseScore {
  try {
    const cleaned = raw.replace(/```json\n?/g, '').replace(/```\n?/g, '').trim();
    const parsed = JSON.parse(cleaned);
    return {
      actionCorrectness: clamp(parsed.actionCorrectness, 1, 5),
      toolSelection: clamp(parsed.toolSelection, 1, 5),
      minimalAction: clamp(parsed.minimalAction, 1, 5),
      overall: clamp(parsed.overall, 1, 5),
      reasoning: parsed.reasoning || '',
    };
  } catch {
    return defaultResponseScore(`Parse error: ${raw.substring(0, 100)}`);
  }
}

function defaultInteractionScore(reasoning: string): InteractionScore {
  return { contextRelevance: 3, specificity: 3, appropriateness: 3, metadataQuality: 3, overall: 3, reasoning };
}

function defaultResponseScore(reasoning: string): ResponseScore {
  return { actionCorrectness: 3, toolSelection: 3, minimalAction: 3, overall: 3, reasoning };
}

function clamp(value: number, min: number, max: number): number {
  return Math.max(min, Math.min(max, value));
}

function buildUserContextSummary(userData: LoadedUserData): string {
  const p = userData.profile;
  const name = p.preferred_name || p.display_name || p.email;
  const occupation = p.occupation ? `Occupation: ${p.occupation}` : '';
  const aspects = (userData.aspects as any[]).map(a => a.name).join(', ');
  const tasks = (userData.tasks as any[]).map(t => t.title).join(', ');
  const goals = (userData.goals as any[]).map(g => g.title).join(', ');

  return [
    `Name: ${name}`,
    occupation,
    `Aspects: ${aspects}`,
    `Upcoming events: ${userData.events.length}`,
    `Active tasks (${userData.tasks.length}): ${tasks}`,
    `Active goals (${userData.goals.length}): ${goals}`,
  ].filter(Boolean).join('\n');
}

function delay(ms: number): Promise<void> {
  return new Promise(resolve => setTimeout(resolve, ms));
}
